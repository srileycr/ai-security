#Day 2 of Python, basics of arithmethics

print (10 + 5) #add
print (10 - 5) # substration
print (10 * 5) #multiplication
print (10 / 5) #division
print (10 // 5) #floor division
print (10 ** 5) #exponent
print (10 % 5) # modulus

#Comparisons 

print (10 == 5) # equal
print (10 != 5) # not equal
print (10 > 5) # greater than
print (10 < 5) # less than
print (10 >= 5) # greater or equal than
print (10 <= 5) # less or equal than

#Increasing 

num = 2
num = num + 1
num += 1

print (num)

#types of numbers

int1 = 11
flo1 = -1.5

print (type(int1))
print (type(flo1))

# Round (to provide a round result) and abs (abstract number)
print (round(flo1))
print (abs(flo1))
